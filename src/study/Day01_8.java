package study;

// Logic_Operator
public class Day01_8 {

    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;
        // !: 부정형 | true -> false, false -> true
        boolean c = !a;
        boolean d = !b;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }

}
