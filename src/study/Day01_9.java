package study;

// Fix_Operator
public class Day01_9 {

    public static void main(String[] args) {

        int a = 1;
        System.out.println(a);

        // 후위형, 변수 사용후 1만큼 증가
        System.out.println(a++);
        System.out.println(a);
        // 전위형, 1만큼 증가후 변수 사용
        System.out.println(++a);

    }
}
