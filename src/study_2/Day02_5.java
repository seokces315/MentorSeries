package study_2;

// Complex_Operator
public class Day02_5 {

    public static void main(String[] args) {

        int x = 10;
        int y = 10;
        
        // 일반적인 대입연산자 사용
        x = x + 10;
        System.out.println(x);

        // 복합대입연산자 사용
        y += 10;
        System.out.println(y);

    }

}
